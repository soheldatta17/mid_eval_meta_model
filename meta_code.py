def _sha256_prefix(path, nbytes=8192):
    """Return first-8K SHA-256 hex for quick de-dup."""
    h = hashlib.sha256()
    with open(path, "rb") as f:
        h.update(f.read(nbytes))
    return h.hexdigest()


